# # Лабораторная работа 4
# ## Комплексный анализ результатов параметрического исследования
#
# В данном файле выполняется визуализация результатов, полученных
# на этапе сканирования коэффициента заражения `β`.
#
# В отличие от предыдущих файлов, здесь не запускается сама модель,
# а используются уже сохранённые результаты вычислительных экспериментов.
# Цель состоит в том, чтобы представить основные показатели эпидемии
# в удобной графической форме и выполнить их совместный анализ.
#
# На рисунке будут показаны:
#
# - пик эпидемии;
# - конечная доля инфицированных;
# - число умерших;
# - доля выздоровевших.
#
# Это позволяет оценить, как изменение коэффициента заражения влияет
# на разные характеристики эпидемического процесса.

using DrWatson
@quickactivate "project"

using Agents, DataFrames, Plots, CSV

include(srcdir("sir_model.jl"))

# ## Загрузка результатов
#
# Считаем ранее сохранённую таблицу результатов параметрического
# исследования из CSV-файла.

df = CSV.read(datadir("beta_scan_all.csv"), DataFrame)

# ## Построение первого графика
#
# На первом графике отображаются:
#
# - пик эпидемии;
# - конечная доля инфицированных.
#
# Это позволяет сравнить максимальный масштаб вспышки
# и итоговый уровень распространения инфекции.

p1 = plot(
    df.beta,
    df.peak,
    label = "Пик",
    xlabel = "β",
    ylabel = "Доля инфицированных",
)

plot!(
    p1,
    df.beta,
    df.final_inf,
    label = "Конечная",
)

# ## Построение второго графика
#
# На втором графике отображается число умерших
# в зависимости от коэффициента заражения.

p2 = plot(
    df.beta,
    df.deaths,
    xlabel = "β",
    ylabel = "Число умерших",
    label = "Умершие",
)

# ## Построение третьего графика
#
# На третьем графике отображается доля выздоровевших
# в зависимости от коэффициента заражения.

p3 = plot(
    df.beta,
    df.final_rec,
    xlabel = "β",
    ylabel = "Доля выздоровевших",
    label = "Выздоровевшие",
)

# ## Формирование составного рисунка
#
# Объединим три графика в один составной рисунок
# для совместного анализа результатов.

plot(
    p1,
    p2,
    p3,
    layout = (3, 1),
    size = (800, 900),
)

savefig(plotsdir("comprehensive_analysis.png"))

# ## Вывод
#
# В результате был построен составной рисунок, отражающий влияние
# коэффициента заражения `β` на основные характеристики эпидемии.
# Совместный анализ этих графиков позволяет оценить не только
# интенсивность вспышки, но и её последствия с точки зрения
# смертности и выздоровления.
