# # Лабораторная работа №5
# ## Аппарат сетей Петри: анимация процесса
#
# В данном скрипте строится анимация изменения маркировки сети Петри
# для задачи обедающих философов.
#
# Цель анимации — наглядно показать, как во времени меняются состояния
# системы и как может проявляться взаимная блокировка в классической модели.

using DrWatson
@quickactivate "project"

# ## Подключение модели и библиотек
#
# Подключается модуль `DiningPhilosophers` из каталога `src`,
# а также библиотека `Plots` для построения анимации.

include(srcdir("DiningPhilosophers.jl"))
using .DiningPhilosophers

using Plots
using DataFrames

# ## Параметры моделирования
#
# Для анимации используется классическая сеть Петри.
# В методичке предлагается взять:
# - число философов `N = 3`;
# - время моделирования `tmax = 30.0`;
# - частоту кадров `fps = 5`.
#
# Небольшое значение `N` выбирается для того, чтобы анимация
# оставалась простой и наглядной.

N = 3
tmax = 30.0
fps = 5

println("Запуск анимации для классической сети Петри...")

# ## Построение сети и запуск симуляции
#
# Сначала создаётся классическая сеть Петри.
# Затем выполняется стохастическая симуляция,
# результаты которой используются для создания кадров анимации.

net, u0, place_names = build_classical_network(N)
df = simulate_stochastic(net, u0, tmax)

# ## Построение анимации
#
# Каждый кадр анимации представляет собой столбчатую диаграмму,
# показывающую текущее число фишек в каждой позиции сети.
# Таким образом можно наглядно проследить изменение маркировки во времени.

anim = @animate for k in 1:nrow(df)
    values = [df[k, String(name)] for name in place_names]

    bar(
        string.(place_names),
        values;
        xlabel = "Позиции сети Петри",
        ylabel = "Число фишек",
        title = "Маркировка сети Петри, t = $(round(df[k, :time], digits=2))",
        legend = false,
        xticks = :all,
        xrotation = 45,
        size = (1000, 500)
    )
end

# ## Сохранение результата
#
# Итогом работы является GIF-файл, который сохраняется
# в каталог `plots/`.

gif(anim, plotsdir("philosophers_simulation.gif"), fps = fps)

println("Анимация сохранена в plots/philosophers_simulation.gif")

# ## Интерпретация
#
# Анимация позволяет увидеть, как фишки перемещаются между позициями,
# то есть как философы переходят между состояниями Think, Hungry и Eat.
# Если на последних кадрах маркировка перестаёт изменяться,
# это указывает на возникновение deadlock в классической модели.
