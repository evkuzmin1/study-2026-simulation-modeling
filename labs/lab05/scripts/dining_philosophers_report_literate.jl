# # Лабораторная работа №5
# ## Аппарат сетей Петри: итоговый сравнительный отчёт
#
# В данном скрипте выполняется сравнительный анализ двух моделей
# задачи обедающих философов:
# - классической сети Петри;
# - сети Петри с арбитром.
#
# Основной показатель сравнения — число философов,
# находящихся в состоянии `Eat`.
#
# Скрипт не запускает моделирование заново,
# а использует уже сохранённые результаты из CSV-файлов.

using DrWatson
@quickactivate "project"

# ## Подключение библиотек
#
# Используются:
# - `CSV` и `DataFrames` для чтения результатов моделирования;
# - `Plots` для построения итогового сравнительного графика.

using DataFrames
using CSV
using Plots

# ## Загрузка входных данных
#
# Загружаются CSV-файлы, сформированные ранее скриптом
# `dining_philosophers.jl`.
#
# Если этих файлов нет, скрипт завершится ошибкой,
# поэтому сначала должны быть выполнены основные эксперименты.

df_classic = CSV.read(datadir("dining_classic.csv"), DataFrame)
df_arbiter = CSV.read(datadir("dining_arbiter.csv"), DataFrame)

# ## Задание числа философов
#
# В рассматриваемом примере используется `N = 5`.
# Это соответствует исходной постановке эксперимента.

N = 5

# ## Выбор столбцов состояния `Eat`
#
# Из таблиц извлекаются столбцы `Eat_i`,
# показывающие, находится ли философ в состоянии приёма пищи
# в каждый момент времени.

eat_cols = [Symbol("Eat_$i") for i in 1:N]

# ## Построение графика для классической сети
#
# На первом графике отображается динамика состояния `Eat`
# для философов в классической сети Петри.

p1 = plot(
    df_classic.time,
    Matrix(df_classic[:, eat_cols]),
    label = ["Ф $i" for i in 1:N],
    xlabel = "Время",
    ylabel = "Ест (1/0)",
    title = "Классическая сеть",
)

# ## Построение графика для сети с арбитром
#
# На втором графике отображается динамика состояния `Eat`
# для сети Петри с арбитром.

p2 = plot(
    df_arbiter.time,
    Matrix(df_arbiter[:, eat_cols]),
    label = ["Ф $i" for i in 1:N],
    xlabel = "Время",
    ylabel = "Ест (1/0)",
    title = "Сеть с арбитром",
)

# ## Формирование итогового рисунка
#
# Два построенных графика объединяются в один рисунок
# с двумя панелями, расположенными друг под другом.

p_final = plot(p1, p2, layout = (2, 1), size = (800, 600))

# ## Сохранение результата
#
# Итоговый сравнительный график сохраняется в папку `plots/`
# под именем `final_report.png`.

savefig(p_final, plotsdir("final_report.png"))

println("Отчёт сохранён в plots/final_report.png")

# ## Интерпретация результата
#
# В классической сети возможно появление deadlock:
# через некоторое время линии `Eat_i` могут опуститься до нуля
# и больше не изменяться, что означает прекращение доступа философов к еде.
#
# В сети с арбитром линии `Eat_i` продолжают изменяться во времени,
# что указывает на отсутствие взаимной блокировки
# и более устойчивую работу системы.
#
# Таким образом, итоговый график служит наглядным сравнением
# эффективности двух подходов.
