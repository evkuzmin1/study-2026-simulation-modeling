# # Анимация модели Daisyworld
#
# В данной работе строится анимация агентной модели Daisyworld.
# Целью является визуализация изменения состояния системы во времени.
#
# В анимации:
# - цвет фона соответствует температуре поверхности;
# - белые и чёрные ромашки отображаются в соответствии со своим типом;
# - результат сохраняется в видеофайл формата mp4.

using DrWatson
@quickactivate "project"

# ## Подключение библиотек

using Agents
using DataFrames
using Plots
using CairoMakie

# ## Подключение модели

include(srcdir("daisyworld.jl"))

# ## Создание модели

model = daisyworld()

# ## Настройка отображения агентов
#
# Цвет ромашек определяется их типом.

daisycolor(a::Daisy) = a.breed

plotkwargs = (
    agent_color = daisycolor,
    agent_size = 20,
    agent_marker = '✿',
    heatarray = :temperature,
    heatkwargs = (colorrange = (-20, 60),),
)

# ## Построение анимации
#
# Анимация сохраняется в файл `simulation.mp4` в папке `plots`.

abmvideo(
    plotsdir("simulation.mp4"),
    model;
    title = "Daisy World",
    frames = 60,
    plotkwargs...,
)

println("Готово: создан файл simulation.mp4")

# ## Анализ результата
#
# По построенной анимации можно сделать следующие выводы:
#
# 1. Состояние модели меняется во времени, что позволяет наблюдать динамику
#    распределения ромашек и температурного поля.
#
# 2. Изменение пространственного распределения белых и чёрных ромашек
#    влияет на температурный режим поверхности.
#
# 3. Анимация наглядно показывает процесс самоорганизации системы.
#
# Таким образом, видеовизуализация позволяет проследить эволюцию модели
